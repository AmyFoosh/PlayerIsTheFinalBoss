// -- SPRITE CLASS --

/*  
    This class is the core for any canvas display object.
    This class is used to create Player and Enemy.
 */

class Sprite {

    // Set initial values for coordinates and object radius size.
    constructor(x, y, radius) {

        this.x = x;
        this.y = y;
        this.radius = radius;
    }
}

